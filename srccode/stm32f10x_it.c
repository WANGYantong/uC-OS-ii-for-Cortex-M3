void USART1_IRQHandler(void)
{
	if (USART_GetITStatus(USART1, USART_IT_RXNE) == SET) {
		USART_SendData(USART1, USART_ReceiveData(USART1));
		while (USART_GetFlagStatus(USART1, USART_FLAG_TXE) == RESET);	//判断是否发送完成
	}
}


